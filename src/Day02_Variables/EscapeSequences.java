package Day02_Variables;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("\tHello");
        System.out.println("\t\t\t\t\t\tHello");
        // \t means a pragraph space
        System.out.println("Hello \nWorld");
        System.out.println("My\nName\nIs\nAynur");
        // \n means start a new line

        System.out.println("My Favorite TV Show is \"The Game of Throne\"");
        // /"prings double quote " on the console
        System.out.println("My favorite TV show is 'The Game Of Throne'");
        System.out.println("My favorite TV show is \'The Game of Throne\'");

    }
}
