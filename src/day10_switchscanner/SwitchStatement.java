package day10_switchscanner;

public class SwitchStatement {
    public static void main(String[] args) {
        switch (4){
            case 1:
                System.out.println("one");
                break;

            case 2:
                System.out.println("Two");
                break;

            default:
                System.out.println("Invalid Case");
                break;

        }



    }
}
